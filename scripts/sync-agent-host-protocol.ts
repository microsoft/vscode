function getSourceCommitHash(): string {
	try {
		return execSync('git rev-parse --short HEAD', { cwd: PROTOCOL_REPO, encoding: 'utf-8' }).trim();
	} catch {
		return 'unknown';
	}
}

function stripExistingHeader(content: string): string {
	return content.replace(/^\/\*\*?[\s\S]*?\*\/\s*/, '');
}

function convertIndentation(content: string): string {
	const lines = content.split('\n');
	return lines.map(line => {
		const match = line.match(/^( +)/);
		if (!match) {
			return line;
		}
		const spaces = match[1].length;
		const tabs = Math.floor(spaces / 2);
		const remainder = spaces % 2;
		return '\t'.repeat(tabs) + ' '.repeat(remainder) + line.slice(spaces);
	}).join('\n');
}

/**
 * For state.ts: replace `export type URI = string;` with an import of VS Code's URI.
 */
function transformStateFile(content: string): string {
	// Remove the URI type alias and its JSDoc comment
	content = content.replace(/(?:\/\*\*[\s\S]*?\*\/\s*)?export type URI = string;\s*/, '');
	// Add URI import at the top (after any remaining leading content)
	content = `import { URI } from '${URI_IMPORT_PATH}';\n\n` + content;
	return content;
}

/**
 * For files that `import type { URI, ... } from './state.js'`:
 * Remove URI from the type import and add a separate value import of URI from uri.js.
 */
function transformUriImports(content: string, depth: number): string {
	const uriPath = depth === 0 ? URI_IMPORT_PATH : '../' + URI_IMPORT_PATH;

	// Handle `import type { URI, X, Y } from './state.js'`
	const typeImportRegex = /import type \{([^}]*)\} from '(\.\/state\.js|\.\.\/state\.js)';/g;
	let needsUriImport = false;

	content = content.replace(typeImportRegex, (_match, names: string, from: string) => {
		const parts = names.split(',').map((s: string) => s.trim()).filter((s: string) => s.length > 0);
		const hasUri = parts.some((p: string) => p === 'URI');
		if (!hasUri) {
			return _match; // no URI in this import, leave as-is
		}
		needsUriImport = true;
		const remaining = parts.filter((p: string) => p !== 'URI');
		if (remaining.length === 0) {
			return ''; // only URI was imported
		}
		return `import type { ${remaining.join(', ')} } from '${from}';`;
	});

	// Handle `import { ToolCallConfirmationReason, ... } from './state.js'`
	// (value imports that might also import URI — unlikely but handle it)
	const valueImportRegex = /import \{([^}]*)\} from '(\.\/state\.js|\.\.\/state\.js)';/g;
	content = content.replace(valueImportRegex, (_match, names: string, from: string) => {
		const parts = names.split(',').map((s: string) => s.trim()).filter((s: string) => s.length > 0);
		const hasUri = parts.some((p: string) => p === 'URI');
		if (!hasUri) {
			return _match;
		}
		needsUriImport = true;
		const remaining = parts.filter((p: string) => p !== 'URI');
		if (remaining.length === 0) {
			return '';
		}
		return `import { ${remaining.join(', ')} } from '${from}';`;
	});

	if (needsUriImport) {
		// Add URI import at the top
		content = `import { URI } from '${uriPath}';\n` + content;
	}

	return content;
}

/**
 * Merges duplicate imports from the same module.
 * Combines `import type { A }` and `import { B }` from the same module into
 * `import { B, type A }` to satisfy the no-duplicate-imports lint rule.
 */
function mergeDuplicateImports(content: string): string {
	// Collect all imports grouped by module path
	const importsByModule = new Map<string, { typeNames: string[]; valueNames: string[] }>();
	const otherLines: string[] = [];
	const seenModules = new Set<string>();

	for (const line of content.split('\n')) {
		const typeMatch = line.match(/^import type \{([^}]+)\} from '([^']+)';$/);
		const valueMatch = line.match(/^import \{([^}]+)\} from '([^']+)';$/);

		if (typeMatch) {
			const [, names, mod] = typeMatch;
			if (!importsByModule.has(mod)) {
				importsByModule.set(mod, { typeNames: [], valueNames: [] });
			}
			importsByModule.get(mod)!.typeNames.push(...names.split(',').map(s => s.trim()).filter(s => s.length > 0));
			if (!seenModules.has(mod)) {
				seenModules.add(mod);
				otherLines.push(`__IMPORT_PLACEHOLDER__${mod}`);
			}
		} else if (valueMatch) {
			const [, names, mod] = valueMatch;
			if (!importsByModule.has(mod)) {
				importsByModule.set(mod, { typeNames: [], valueNames: [] });
			}
			importsByModule.get(mod)!.valueNames.push(...names.split(',').map(s => s.trim()).filter(s => s.length > 0));
			if (!seenModules.has(mod)) {
				seenModules.add(mod);
				otherLines.push(`__IMPORT_PLACEHOLDER__${mod}`);
			}
		} else {
			otherLines.push(line);
		}
	}

	// Replace placeholders with merged imports
	return otherLines.map(line => {
		if (line.startsWith('__IMPORT_PLACEHOLDER__')) {
			const mod = line.substring('__IMPORT_PLACEHOLDER__'.length);
			const entry = importsByModule.get(mod)!;
			const uniqueTypes = [...new Set(entry.typeNames)];
			const uniqueValues = [...new Set(entry.valueNames)];

			if (uniqueValues.length > 0 && uniqueTypes.length > 0) {
				// Merge into a single import: `import { Value, type Type } from '...';`
				const allNames = [...uniqueValues, ...uniqueTypes.map(n => `type ${n}`)];
				return `import { ${allNames.join(', ')} } from '${mod}';`;
			} else if (uniqueValues.length > 0) {
				return `import { ${uniqueValues.join(', ')} } from '${mod}';`;
			} else {
				return `import type { ${uniqueTypes.join(', ')} } from '${mod}';`;
			}
		}
		return line;
	}).join('\n');
}

function processFile(src: string, dest: string, commitHash: string): void {
	let content = fs.readFileSync(src, 'utf-8');
	content = stripExistingHeader(content);

	const depth = (dest.match(/\//g) || []).length; // 0 for top-level, 1 for version/

	if (dest === 'state.ts') {
		content = transformStateFile(content);
	} else {
		content = transformUriImports(content, depth);
	}

	// Convert `const enum` to plain `const` object + string literal union.
	// This is necessary because VS Code's tsconfig has `preserveConstEnums: true`
	// which makes `const enum` members nominal types that don't accept raw string
	// literal assignments. The `const` object + union pattern preserves both the
	// namespace (e.g. `SessionStatus.Idle`) and string literal assignability.
	content = convertConstEnums(content);

	// Merge duplicate imports from the same module (e.g. two imports from './state.js')
	content = mergeDuplicateImports(content);

	content = convertIndentation(content);
	content = content.split('\n').map(line => line.trimEnd()).join('\n');

	const header = `${COPYRIGHT}\n\n${BANNER}\n// Synced from agent-host-protocol @ ${commitHash}\n`;
	content = header + '\n' + content;

	if (!content.endsWith('\n')) {
		content += '\n';
	}

	const destPath = path.join(DEST_DIR, dest);
	fs.mkdirSync(path.dirname(destPath), { recursive: true });
	content = formatTypeScript(content, dest);
	fs.writeFileSync(destPath, content, 'utf-8');
	console.log(`  ${dest}`);
}

// Global enum definitions collected from all files before per-file processing
let globalEnumDefs = new Map<string, Map<string, string>>();

function collectAllEnumDefs(): void {
	globalEnumDefs = new Map();
	for (const file of FILES) {
		const srcPath = path.join(TYPES_DIR, file.src);
		if (!fs.existsSync(srcPath)) {
			continue;
		}
		const content = fs.readFileSync(srcPath, 'utf-8');
		content.replace(
			/export const enum (\w+) \{([^}]+)\}/g,
			(_match, name: string, body: string) => {
				const members = new Map<string, string>();
				for (const line of body.split('\n')) {
					const memberMatch = line.match(/^\s*(\w+)\s*=\s*'([^']+)'/);
					if (memberMatch) {
						members.set(memberMatch[1], memberMatch[2]);
					}
				}
				if (members.size > 0) {
					globalEnumDefs.set(name, members);
				}
				return _match;
			}
		);
	}
}

/**
 * Converts `const enum Foo { A = 'a', B = 'b' }` into:
 * ```
 * export const Foo = { A: 'a', B: 'b' } as const;
 * export type Foo = typeof Foo[keyof typeof Foo];
 * ```
 * Then replaces `Foo.A` in type positions with the string literal `'a'`,
 * using the global enum definitions collected from all protocol files.
 */
function convertConstEnums(content: string): string {
	// Replace the const enum declarations in this file
	content = content.replace(
		/export const enum (\w+) \{([^}]+)\}/g,
		(_match, name: string) => {
			const members = globalEnumDefs.get(name);
			if (!members) {
				return _match;
			}
			const objEntries = [...members.entries()].map(([k, v]) => `  ${k}: '${v}'`).join(',\n');
			return `export const ${name} = {\n${objEntries},\n} as const;\nexport type ${name} = typeof ${name}[keyof typeof ${name}];`;
		}
	);

	// Replace Enum.Member references with their resolved string literals,
	// using all globally-known enums (not just those defined in this file).
	for (const [enumName, members] of globalEnumDefs) {
		for (const [memberName, value] of members) {
			const ref = `${enumName}.${memberName}`;
			content = content.split(ref).join(`'${value}'`);
		}
	}

	// Remove value imports of enums that are no longer referenced as values
	// (because all their member accesses were replaced with string literals).
	content = content.replace(
		/import \{([^}]+)\} from '([^']+)';/g,
		(_match, names: string, from: string) => {
			const parts = names.split(',').map((s: string) => s.trim()).filter((s: string) => s.length > 0);
			const remaining = parts.filter((name: string) => {
				// Keep this import if the name is still referenced as a value elsewhere
				// (not just in a type position). Check if the name appears followed by
				// something other than `.` (which would be a member access).
				if (!globalEnumDefs.has(name)) {
					return true; // not an enum, keep
				}
				// Check if the enum name is still used in the content (as a value, not in the import itself)
				const uses = content.split(name).length - 1;
				// Subtract the use in this import line
				return uses > 1;
			});
			if (remaining.length === 0) {
				return '';
			}
			if (remaining.length === parts.length) {
				return _match;
			}
			return `import { ${remaining.join(', ')} } from '${from}';`;
		}
	);

	return content;
}

// ─── URI field detection via TypeScript AST ──────────────────────────────────

interface UriFieldInfo {
	/** Interface name, e.g. 'ISessionSummary' */
	interfaceName: string;
	/** Field name, e.g. 'resource' */
	fieldName: string;
	/** Whether it's an array (URI[]) */
	isArray: boolean;
	/** Whether it's optional */
	isOptional: boolean;
	/** Which source file */
	sourceFile: string;
}

function findUriFields(): UriFieldInfo[] {
	const fields: UriFieldInfo[] = [];

	for (const file of ['state.ts', 'actions.ts', 'commands.ts', 'notifications.ts']) {
		const filePath = path.join(TYPES_DIR, file);
		if (!fs.existsSync(filePath)) {
			continue;
		}
		const source = fs.readFileSync(filePath, 'utf-8');
		const sf = ts.createSourceFile(file, source, ts.ScriptTarget.Latest, true);

		ts.forEachChild(sf, node => {
			if (!ts.isInterfaceDeclaration(node)) {
				return;
			}
			if (!node.modifiers?.some(m => m.kind === ts.SyntaxKind.ExportKeyword)) {
				return; // only exported interfaces
			}
			const interfaceName = node.name.text;
			for (const member of node.members) {
				if (!ts.isPropertySignature(member) || !member.type || !member.name) {
					continue;
				}
				const fieldName = ts.isIdentifier(member.name) ? member.name.text : undefined;
				if (!fieldName) {
					continue;
				}
				const isOptional = !!member.questionToken;
				const typeText = member.type.getText(sf);

				if (typeText === 'URI') {
					fields.push({ interfaceName, fieldName, isArray: false, isOptional, sourceFile: file });
				} else if (typeText === 'URI[]') {
					fields.push({ interfaceName, fieldName, isArray: true, isOptional, sourceFile: file });
				}
			}
		});
	}

	return fields;
}

// ─── Method mapping detection via JSDoc @method tags ─────────────────────────

interface MethodMapping {
	/** JSON-RPC method name, e.g. 'initialize' */
	method: string;
	/** Params interface name, e.g. 'IInitializeParams' */
	paramType: string;
	/** Whether this is a Request or Notification */
	messageType: 'Request' | 'Notification';
	/** True if the params type has a field of type IStateAction (nested action revival) */
	hasNestedAction: boolean;
	/** Field names that have type IStateAction */
	actionFields: string[];
}

/**
 * Parses @method and @messageType JSDoc tags from commands.ts to build a
 * mapping from protocol method names to their params interface names.
 */
function findMethodMappings(): MethodMapping[] {
	const filePath = path.join(TYPES_DIR, 'commands.ts');
	if (!fs.existsSync(filePath)) {
		return [];
	}

	const source = fs.readFileSync(filePath, 'utf-8');
	const sf = ts.createSourceFile('commands.ts', source, ts.ScriptTarget.Latest, true);
	const mappings: MethodMapping[] = [];

	ts.forEachChild(sf, node => {
		if (!ts.isInterfaceDeclaration(node)) {
			return;
		}
		if (!node.name.text.endsWith('Params')) {
			return;
		}

		// Extract @method and @messageType from JSDoc
		const jsDocTags = ts.getJSDocTags(node);
		let method: string | undefined;
		let messageType: string | undefined;

		for (const tag of jsDocTags) {
			const comment = typeof tag.comment === 'string' ? tag.comment.trim() : '';
			if (tag.tagName.text === 'method') {
				method = comment;
			} else if (tag.tagName.text === 'messageType') {
				messageType = comment;
			}
		}

		if (!method || !messageType) {
			return;
		}

		// Check if any field has type IStateAction (for nested action revival)
		let hasNestedAction = false;
		const actionFields: string[] = [];
		for (const member of node.members) {
			if (ts.isPropertySignature(member) && member.type && member.name) {
				const typeText = member.type.getText(sf);
				if (typeText === 'IStateAction') {
					hasNestedAction = true;
					const name = ts.isIdentifier(member.name) ? member.name.text : undefined;
					if (name) {
						actionFields.push(name);
					}
				}
			}
		}

		mappings.push({
			method,
			paramType: node.name.text,
			messageType: messageType as 'Request' | 'Notification',
			hasNestedAction,
			actionFields,
		});
	});

	return mappings;
}

// ─── Serialization file generation ───────────────────────────────────────────

function generateSerializationFile(fields: UriFieldInfo[], methodMappings: MethodMapping[], commitHash: string): void {
	// Group fields by interface and source
	const byInterface = new Map<string, UriFieldInfo[]>();
	for (const f of fields) {
		const key = f.interfaceName;
		if (!byInterface.has(key)) {
			byInterface.set(key, []);
		}
		byInterface.get(key)!.push(f);
	}

	// Separate into session-action types (just {session: URI}) and others
	const sessionActionTypes: string[] = [];
	const otherTypes = new Map<string, UriFieldInfo[]>();

	for (const [iface, infos] of byInterface) {
		if (infos.length === 1 && infos[0].fieldName === 'session' && !infos[0].isArray && !infos[0].isOptional) {
			sessionActionTypes.push(iface);
		} else {
			otherTypes.set(iface, infos);
		}
	}

	// Classify each protocol method (deduplicate by method name)
	const sessionActionTypeSet = new Set(sessionActionTypes);
	const sessionMethods: string[] = [];
	const otherMethodCases: { method: string; paramType: string }[] = [];
	const nestedActionMethods: { method: string; paramType: string; actionFields: string[] }[] = [];
	const seenMethods = new Set<string>();

	for (const mapping of methodMappings) {
		if (seenMethods.has(mapping.method)) {
			continue;
		}
		if (sessionActionTypeSet.has(mapping.paramType)) {
			seenMethods.add(mapping.method);
			sessionMethods.push(mapping.method);
		} else if (otherTypes.has(mapping.paramType)) {
			seenMethods.add(mapping.method);
			otherMethodCases.push({ method: mapping.method, paramType: mapping.paramType });
		} else if (mapping.hasNestedAction) {
			seenMethods.add(mapping.method);
			nestedActionMethods.push({ method: mapping.method, paramType: mapping.paramType, actionFields: mapping.actionFields });
		}
	}

	// Only import types that are actually referenced in per-type helpers
	const neededOtherTypes = new Set(otherMethodCases.map(c => c.paramType));
	const hasNestedActions = nestedActionMethods.length > 0;
	// Nested-action param types need imports too
	const nestedActionParamTypes = new Set<string>();
	for (const { paramType } of nestedActionMethods) {
		neededOtherTypes.add(paramType);
		nestedActionParamTypes.add(paramType);
	}
	const usedTypes = new Set<string>([...sessionActionTypes, ...neededOtherTypes]);
	const importsBySource = new Map<string, Set<string>>();
	for (const [iface, infos] of byInterface) {
		if (!usedTypes.has(iface)) {
			continue;
		}
		const source = infos[0].sourceFile;
		if (!importsBySource.has(source)) {
			importsBySource.set(source, new Set());
		}
		importsBySource.get(source)!.add(iface);
	}
	// Nested-action param types may not be in byInterface (no direct URI fields),
	// so add them to commands.ts imports explicitly.
	for (const paramType of nestedActionParamTypes) {
		if (!byInterface.has(paramType)) {
			if (!importsBySource.has('commands.ts')) {
				importsBySource.set('commands.ts', new Set());
			}
			importsBySource.get('commands.ts')!.add(paramType);
		}
	}

	const sourceToModule: Record<string, string> = {
		'state.ts': './state.js',
		'actions.ts': './actions.js',
		'commands.ts': './commands.js',
		'notifications.ts': './notifications.js',
	};

	const lines: string[] = [];
	lines.push(COPYRIGHT);
	lines.push('');
	lines.push(BANNER);
	lines.push(`// Synced from agent-host-protocol @ ${commitHash}`);
	lines.push('');
	lines.push('// Generated revive/serialize helpers for converting between wire-format');
	lines.push('// (URI as string) and VS Code types (URI as class instance).');
	lines.push('');
	lines.push(`import { URI } from '${URI_IMPORT_PATH}';`);

	for (const [source, types] of importsBySource) {
		const module = sourceToModule[source];
		if (module) {
			// If we need IStateAction for nested-action revival, add it to the actions import
			if (hasNestedActions && module === './actions.js') {
				types.add('IStateAction');
			}
			lines.push(`import type { ${[...types].sort().join(', ')} } from '${module}';`);
		}
	}
	lines.push(`import type { IAhpRequest, IAhpClientNotification, ICommandMap, IClientNotificationMap, IJsonRpcResponse } from './messages.js';`);

	lines.push('');
	lines.push('// ---- Wire<T> helper --------------------------------------------------------');
	lines.push('');
	lines.push('/**');
	lines.push(' * Recursively maps a protocol type to its JSON wire format by replacing URI');
	lines.push(' * fields with plain strings. Distributes over unions so');
	lines.push(' * `Wire<URI | undefined>` = `string | undefined`.');
	lines.push(' */');
	lines.push('export type Wire<T> =');
	lines.push('\tT extends URI ? string :');
	lines.push('\tT extends URI[] ? string[] :');
	lines.push('\tT extends readonly (infer U)[] ? Wire<U>[] :');
	lines.push('\tT extends object ? { [K in keyof T]: Wire<T[K]> } :');
	lines.push('\tT;');

	// ---- Wire command/notification maps ----
	lines.push('');
	lines.push('// ---- Wire message maps -----------------------------------------------------');
	lines.push('');
	lines.push('/** ICommandMap with URI params replaced by strings. */');
	lines.push('type WireCommandMap = { [M in keyof ICommandMap]: { params: Wire<ICommandMap[M][\'params\']>; result: ICommandMap[M][\'result\'] } };');
	lines.push('');
	lines.push('/** IClientNotificationMap with URI params replaced by strings. */');
	lines.push('type WireClientNotificationMap = { [M in keyof IClientNotificationMap]: { params: Wire<IClientNotificationMap[M][\'params\']> } };');
	lines.push('');
	lines.push('/** A wire-format request (params have string instead of URI). */');
	lines.push('type WireAhpRequest<M extends keyof WireCommandMap = keyof WireCommandMap> =');
	lines.push('\tM extends unknown ? { readonly jsonrpc: \'2.0\'; readonly id: number; readonly method: M; readonly params: WireCommandMap[M][\'params\'] } : never;');
	lines.push('');
	lines.push('/** A wire-format client notification (params have string instead of URI). */');
	lines.push('type WireAhpClientNotification<M extends keyof WireClientNotificationMap = keyof WireClientNotificationMap> =');
	lines.push('\tM extends unknown ? { readonly jsonrpc: \'2.0\'; readonly method: M; readonly params: WireClientNotificationMap[M][\'params\'] } : never;');
	lines.push('');
	lines.push('/** Any message as it arrives from JSON.parse (URIs are plain strings). */');
	lines.push('export type WireAhpMessage = WireAhpRequest | WireAhpClientNotification | IJsonRpcResponse;');

	// ---- Private revive helpers ----
	if (sessionActionTypes.length > 0) {
		lines.push('');
		lines.push('// ---- Session action helpers (all have `session: URI`) ----------------------');
		lines.push('');
		lines.push(`type SessionAction = ${sessionActionTypes.join(' | ')};`);
		lines.push('type WireSessionAction = Wire<SessionAction>;');
		lines.push('');
		lines.push('function reviveSessionAction(wire: WireSessionAction): SessionAction {');
		lines.push('\treturn { ...wire, session: URI.parse(wire.session) };');
		lines.push('}');
	}

	// Generate reviveStateAction for the full IStateAction union (session + root).
	// Root actions have no URI fields so they pass through unchanged.
	if (hasNestedActions && sessionActionTypes.length > 0) {
		lines.push('');
		lines.push('function reviveStateAction(wire: Wire<IStateAction>): IStateAction {');
		lines.push('\tif (\'session\' in wire && typeof wire.session === \'string\') {');
		lines.push('\t\treturn { ...wire, session: URI.parse(wire.session) } as IStateAction;');
		lines.push('\t}');
		lines.push('\treturn wire as IStateAction;');
		lines.push('}');
	}

	lines.push('');
	lines.push('// ---- Individual helpers (private) ------------------------------------------');

	for (const [iface, infos] of otherTypes) {
		if (!neededOtherTypes.has(iface)) {
			continue;
		}
		lines.push('');
		const wireType = `Wire<${iface}>`;
		lines.push(`function revive${iface.replace(/^I/, '')}(wire: ${wireType}): ${iface} {`);
		lines.push('\treturn {');
		lines.push('\t\t...wire,');
		for (const f of infos) {
			if (f.isArray) {
				if (f.isOptional) {
					lines.push(`\t\t${f.fieldName}: wire.${f.fieldName}?.map(s => URI.parse(s)),`);
				} else {
					lines.push(`\t\t${f.fieldName}: wire.${f.fieldName}.map(s => URI.parse(s)),`);
				}
			} else {
				if (f.isOptional) {
					lines.push(`\t\t${f.fieldName}: wire.${f.fieldName} !== undefined ? URI.parse(wire.${f.fieldName}) : undefined,`);
				} else {
					lines.push(`\t\t${f.fieldName}: URI.parse(wire.${f.fieldName}),`);
				}
			}
		}
		lines.push('\t};');
		lines.push('}');
	}

	// Generate revive functions for params types that contain nested IStateAction fields
	for (const { paramType, actionFields } of nestedActionMethods) {
		lines.push('');
		const wireType = `Wire<${paramType}>`;
		lines.push(`function revive${paramType.replace(/^I/, '')}(wire: ${wireType}): ${paramType} {`);
		lines.push('\treturn {');
		lines.push('\t\t...wire,');
		for (const field of actionFields) {
			lines.push(`\t\t${field}: reviveStateAction(wire.${field}),`);
		}
		lines.push('\t};');
		lines.push('}');
	}

	// ---- protocolReviver ----
	lines.push('');
	lines.push('// ---- Protocol reviver ------------------------------------------------------');
	lines.push('');
	lines.push('/** Replace `params` on a message while preserving the discriminant. */');
	lines.push('function withParams<T extends { readonly params: unknown }, P>(msg: T, params: P): Omit<T, \'params\'> & { readonly params: P } {');
	lines.push('\treturn { ...msg, params };');
	lines.push('}');
	lines.push('');
	lines.push('/** Fully-typed incoming message after revival. */');
	lines.push('export type AhpIncomingMessage = IAhpRequest | IAhpClientNotification | IJsonRpcResponse;');
	lines.push('');
	lines.push('/**');
	lines.push(' * Revives URI strings in an incoming JSON-RPC message\'s params back to URI');
	lines.push(' * instances. Call once after `JSON.parse` at the transport boundary.');
	lines.push(' *');
	lines.push(' * Accepts a loosely-typed message (`{ jsonrpc, ... }`) and returns a fully');
	lines.push(' * typed {@link AhpIncomingMessage} with narrowable `method` and `params`.');
	lines.push(' * Responses (no `method`) pass through unchanged.');
	lines.push(' */');
	lines.push('export function protocolReviver(msg: { readonly jsonrpc: \'2.0\'; method?: string; params?: unknown; id?: number; result?: unknown; error?: unknown }): AhpIncomingMessage {');
	lines.push('\tif (typeof msg.method !== \'string\') {');
	lines.push('\t\treturn msg as unknown as IJsonRpcResponse;');
	lines.push('\t}');
	lines.push('\t// Narrow to the wire-format discriminated union so that switching on');
	lines.push('\t// `method` gives fully typed `params` in each branch.');
	lines.push('\tconst wire = msg as WireAhpRequest | WireAhpClientNotification;');
	lines.push('\tswitch (wire.method) {');

	// Session-action methods (grouped)
	if (sessionMethods.length > 0) {
		for (const method of sessionMethods) {
			lines.push(`\t\tcase '${method}':`);
		}
		lines.push('\t\t\treturn withParams(wire, reviveSessionAction(wire.params));');
	}

	// Methods with their own revive function
	for (const { method, paramType } of otherMethodCases) {
		const fnName = `revive${paramType.replace(/^I/, '')}`;
		lines.push(`\t\tcase '${method}':`);
		lines.push(`\t\t\treturn withParams(wire, ${fnName}(wire.params));`);
	}

	// Methods with nested IStateAction fields (e.g. dispatchAction)
	for (const { method, paramType } of nestedActionMethods) {
		const fnName = `revive${paramType.replace(/^I/, '')}`;
		lines.push(`\t\tcase '${method}':`);
		lines.push(`\t\t\treturn withParams(wire, ${fnName}(wire.params));`);
	}

	lines.push('\t\tdefault:');
	lines.push('\t\t\treturn wire as AhpIncomingMessage;');
	lines.push('\t}');
	lines.push('}');

	lines.push('');

	// protocolReplacer
	lines.push('// ---- JSON replacer for WebSocket transports --------------------------------');
	lines.push('');
	lines.push('/**');
	lines.push(' * JSON.stringify replacer that converts URI instances to plain strings.');
	lines.push(' * Use with `JSON.stringify(msg, protocolReplacer)` at the transport boundary.');
	lines.push(' */');
	lines.push('export function protocolReplacer(_key: string, value: unknown): unknown {');
	lines.push('\tif (value instanceof URI) {');
	lines.push('\t\treturn value.toString();');
	lines.push('\t}');
	lines.push('\treturn value;');
	lines.push('}');

	lines.push('');
	let content = lines.join('\n') + '\n';
	const destPath = path.join(DEST_DIR, 'protocolSerialization.ts');
	content = formatTypeScript(content, 'protocolSerialization.ts');
	fs.writeFileSync(destPath, content, 'utf-8');
	console.log('  protocolSerialization.ts (generated)');
}

// ---- Main -------------------------------------------------------------------

function main() {
	if (!fs.existsSync(TYPES_DIR)) {
		console.error(`ERROR: Cannot find ${TYPES_DIR}`);
		console.error('Clone agent-host-protocol as a sibling of the VS Code repo:');
		console.error('  git clone git@github.com:microsoft/agent-host-protocol.git ../agent-host-protocol');
		process.exit(1);
	}

	const commitHash = getSourceCommitHash();
	console.log(`Syncing from agent-host-protocol @ ${commitHash}`);
	console.log(`  Source: ${TYPES_DIR}`);
	console.log(`  Dest:   ${DEST_DIR}`);
	console.log();

	// Step 0: Collect all enum definitions across all protocol files
	collectAllEnumDefs();
	console.log(`  Collected ${globalEnumDefs.size} const enums`);

	// Step 1: Copy protocol files
	for (const file of FILES) {
		const srcPath = path.join(TYPES_DIR, file.src);
		if (!fs.existsSync(srcPath)) {
			console.error(`  SKIP (not found): ${file.src}`);
			continue;
		}
		processFile(srcPath, file.dest, commitHash);
	}

	// Step 2: Detect URI fields and method mappings, then generate serialization helpers
	console.log();
	console.log('Generating serialization helpers...');
	const uriFields = findUriFields();
	console.log(`  Found ${uriFields.length} URI fields across ${new Set(uriFields.map(f => f.interfaceName)).size} interfaces`);
	const methodMappings = findMethodMappings();
	console.log(`  Found ${methodMappings.length} method mappings`);
	generateSerializationFile(uriFields, methodMappings, commitHash);

	console.log();
	console.log('Done.');
}

main();

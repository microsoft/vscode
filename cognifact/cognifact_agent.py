class CognifactAgent:
    def __init__(self):
        self.cognifact = None  # The live, self-improving test case
        self.initialize_cognifact()

    def initialize_cognifact(self):
        """Start with a simple base test."""
        self.cognifact = '''def test_addition():
    """Cognifact test case for addition function."""
    assert add(2, 3) == 5, "2+3 should be 5"
    assert add(0, 0) == 0, "0+0 should be 0"
    assert add(-1, 1) == 0, "-1+1 should be 0"
'''

    def write_test(self, specification: str) -> str:
        """Agent generates a new cognifact (test) from a high-level spec."""
        print(f"[Cognifact Agent] Writing new test for specification: '{specification}'")
        self.cognifact = f'''def test_{specification.lower().replace(" ", "_")}():
    """Cognifact test case generated by agent for: {specification}"""
    assert add(2, 3) == 5, "Basic positive addition"
    assert add(-1, 1) == 0, "Negative + positive"
    assert add(0, 5) == 5, "Zero edge case"
    assert add(10, -5) == 5, "Positive + negative"
    print("Cognifact test written and ready for validation.")
'''
        return self.cognifact

    def validate_test(self, implementation_code: str) -> tuple[bool, str]:
        """Run the current cognifact test against the candidate implementation.
        Returns (passed: bool, message: str)"""
        try:
            namespace = {}
            exec(implementation_code, namespace)
            exec(self.cognifact, namespace)

            test_func_name = next((name for name in namespace if name.startswith("test_")), None)
            if test_func_name:
                namespace[test_func_name]()

            return True, "✅ All assertions passed"
        except AssertionError as e:
            return False, f"❌ Assertion failed: {str(e)}"
        except Exception as e:
            return False, f"❌ Error during validation: {type(e).__name__}: {str(e)}"

    def update_test(self, failure_reason: str) -> str:
        """Self-repair: Append a note and extra robustness check when test fails."""
        update_note = f"\n# Auto-updated by agent: {failure_reason}"
        update_note += "\n# Added robustness check: assert add(10, -5) == 5"
        self.cognifact += update_note
        return self.cognifact

    def compute_loss(self, candidate_output, expected_output) -> float:
        """Simple 0/1 loss derived from the cognifact expectations."""
        return 0.0 if candidate_output == expected_output else 1.0

    def get_cognifact(self) -> str:
        """Return the current test case (cognifact)."""
        return self.cognifact


if __name__ == "__main__":
    agent = CognifactAgent()
    print("=== Cognifact Agent Initialized ===\n")
    print("Initial Cognifact:\n")
    print(agent.get_cognifact())

    print("\n" + "="*60)
    agent.write_test("addition function with edge cases")
    print("\nCognifact after write_test():\n")
    print(agent.get_cognifact())

    bad_impl = """def add(a, b):
    return a - b  # buggy: subtraction instead of addition
"""
    print("\n" + "="*60)
    print("Validating against BAD implementation...")
    passed, msg = agent.validate_test(bad_impl)
    print(msg)

    if not passed:
        print("\nUpdating cognifact due to failure...")
        agent.update_test("Implementation returned subtraction instead of addition")
        print("Cognifact after update:\n")
        print(agent.get_cognifact())

    good_impl = """def add(a, b):
    return a + b
"""
    print("\n" + "="*60)
    print("Validating against GOOD implementation...")
    passed, msg = agent.validate_test(good_impl)
    print(msg)

    print("\n" + "="*60)
    print("Using Cognifact as Loss Function:")
    print("Loss for output 5 vs expected 5:", agent.compute_loss(5, 5))
    print("Loss for output 7 vs expected 5:", agent.compute_loss(7, 5))

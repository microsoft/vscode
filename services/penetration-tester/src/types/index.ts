/**
 * OWASP Top 10 categories (2021).
 */
export type OwaspCategory =
	| 'A01-broken-access-control'
	| 'A02-cryptographic-failures'
	| 'A03-injection'
	| 'A04-insecure-design'
	| 'A05-security-misconfiguration'
	| 'A06-vulnerable-components'
	| 'A07-auth-failures'
	| 'A08-software-integrity-failures'
	| 'A09-logging-failures'
	| 'A10-ssrf';

/**
 * Severity classification for vulnerability findings.
 */
export type Severity = 'critical' | 'high' | 'medium' | 'low';

/**
 * Types of security tests the sandbox agents can execute.
 */
export type TestType =
	| 'sql-injection'
	| 'nosql-injection'
	| 'command-injection'
	| 'template-injection'
	| 'xss-reflected'
	| 'xss-stored'
	| 'brute-force'
	| 'session-management'
	| 'password-reset'
	| 'idor'
	| 'privilege-escalation'
	| 'missing-auth'
	| 'zap-baseline'
	| 'zap-active'
	| 'zap-api';

/**
 * Status of a scan session.
 */
export type ScanStatus = 'pending' | 'running' | 'completed' | 'failed' | 'cancelled';

/**
 * An HTTP endpoint discovered during attack surface analysis.
 */
export interface Endpoint {
	method: string;
	path: string;
	parameters: ParameterInfo[];
	authentication: boolean;
	dataFlows: string[];
}

/**
 * Parameter metadata for an endpoint.
 */
export interface ParameterInfo {
	name: string;
	location: 'query' | 'body' | 'path' | 'header';
	type: string;
	required: boolean;
}

/**
 * Attack surface analysis from code graph queries.
 */
export interface AttackSurface {
	endpoints: Endpoint[];
	authMechanisms: string[];
	databaseQueries: DatabaseQueryInfo[];
	userInputHandlers: string[];
	fileUploadHandlers: string[];
	externalApiCalls: string[];
	technologyStack: TechnologyStack;
}

/**
 * Database query information for injection analysis.
 */
export interface DatabaseQueryInfo {
	filePath: string;
	line: number;
	queryType: 'sql' | 'nosql' | 'graphql' | 'cypher';
	parameterized: boolean;
	rawQuery: string;
}

/**
 * Technology stack information.
 */
export interface TechnologyStack {
	framework: string;
	database: string;
	authMethod: string;
	templateEngine: string;
}

/**
 * A test plan generated by the coordinator.
 */
export interface TestPlan {
	id: string;
	targetUrl: string;
	attackSurface: AttackSurface;
	prioritizedCategories: OwaspCategory[];
	tests: PlannedTest[];
	createdAt: number;
}

/**
 * A single test within a test plan.
 */
export interface PlannedTest {
	id: string;
	type: TestType;
	owaspCategory: OwaspCategory;
	targetEndpoint: Endpoint;
	parameters: Record<string, unknown>;
	priority: number;
}

/**
 * Result from executing a single security test.
 */
export interface TestResult {
	testId: string;
	type: TestType;
	owaspCategory: OwaspCategory;
	endpoint: string;
	finding: VulnerabilityFinding | null;
	requestCount: number;
	durationMs: number;
	evidence: TestEvidence;
}

/**
 * Evidence captured during test execution.
 */
export interface TestEvidence {
	requests: RequestRecord[];
	responses: ResponseRecord[];
	observations: string[];
}

/**
 * Record of an HTTP request sent during testing.
 */
export interface RequestRecord {
	method: string;
	url: string;
	headers: Record<string, string>;
	body: string | null;
	timestamp: number;
}

/**
 * Record of an HTTP response received during testing.
 */
export interface ResponseRecord {
	statusCode: number;
	headers: Record<string, string>;
	body: string;
	timestamp: number;
	durationMs: number;
}

/**
 * A suspected vulnerability finding before validation.
 */
export interface VulnerabilityFinding {
	id: string;
	owaspCategory: OwaspCategory;
	testType: TestType;
	severity: Severity;
	endpoint: string;
	payload: string;
	evidence: string;
	impact: string;
	suggestedFix: string;
	filePath?: string;
	line?: number;
	confirmed: boolean;
	reproducible: boolean;
}

/**
 * A validated and confirmed vulnerability report.
 */
export interface VulnerabilityReport {
	id: string;
	finding: VulnerabilityFinding;
	reproductionResult: ReproductionResult;
	impactAssessment: string;
	reproductionSteps: string[];
}

/**
 * Result of attempting to reproduce a finding.
 */
export interface ReproductionResult {
	reproduced: boolean;
	attempts: number;
	matchedResponse: boolean;
	notes: string;
}

/**
 * A complete penetration test session.
 */
export interface ScanSession {
	id: string;
	targetUrl: string;
	status: ScanStatus;
	testPlan: TestPlan | null;
	testResults: TestResult[];
	validatedFindings: VulnerabilityReport[];
	startedAt: number;
	completedAt: number | null;
	requestCount: number;
}

/**
 * Configuration for the penetration testing service.
 */
export interface PenTestConfig {
	port: number;
	zapHost: string;
	zapPort: number;
	targetBaseUrl: string;
	falkordbHost: string;
	falkordbPort: number;
	maxRequestsPerTest: number;
	testTimeoutMs: number;
}

/**
 * SARIF output format for integration with VS Code problems panel.
 */
export interface SarifReport {
	$schema: string;
	version: string;
	runs: SarifRun[];
}

/**
 * A single run in the SARIF report.
 */
export interface SarifRun {
	tool: {
		driver: {
			name: string;
			version: string;
			rules: SarifRule[];
		};
	};
	results: SarifResult[];
}

/**
 * A SARIF rule definition.
 */
export interface SarifRule {
	id: string;
	name: string;
	shortDescription: { text: string };
	helpUri: string;
}

/**
 * A SARIF result entry.
 */
export interface SarifResult {
	ruleId: string;
	level: 'error' | 'warning' | 'note';
	message: { text: string };
	locations: SarifLocation[];
}

/**
 * A SARIF location reference.
 */
export interface SarifLocation {
	physicalLocation: {
		artifactLocation: { uri: string };
		region: {
			startLine: number;
			startColumn: number;
			endLine: number;
			endColumn: number;
		};
	};
}
